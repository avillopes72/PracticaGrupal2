package lop.practicagrupal2;

public class VehiculosTransportesTwo {

    private String codigo;
    private String marca;
    private String modelo;
    private int anioFabricacion;
    private float kilometrajeRecorrido;
    private float costoBaseOperacion;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getAnioFabricacion() {
        return anioFabricacion;
    }

    public void setAnioFabricacion(int anioFabricacion) {
        this.anioFabricacion = anioFabricacion;
    }

    public float getKilometrajeRecorrido() {
        return kilometrajeRecorrido;
    }

    public void setKilometrajeRecorrido(float kilometrajeRecorrido) {
        this.kilometrajeRecorrido = kilometrajeRecorrido;
    }

    public float getCostoBaseOperacion() {
        return costoBaseOperacion;
    }

    public void setCostoBaseOperacion(float costoBaseOperacion) {
        this.costoBaseOperacion = costoBaseOperacion;
    }
    
}
